# example of tuples

tup = (1,2)
print(tup)

# using string 
tup = ('ram', 'sai','for')
print(tup)

# using list
li = [1, 2, 3, 4, 5, 6]
print(tuple(li))

# Using Built-in Function
tup = tuple('coastalseven')

print (tup)


# Creating a Tuple with Mixed Datatypes

tup = (5, 'Welcome', 7.5, True, [1, 2, 3], {'key': 'value'})
print(tup)

# Concatenation of Tuples 

tup1 = (0, 1, 2, 3)
tup2 = (4, 5, 6, 7)
tup3 = tup1 + tup2
print(tup3)

# Slicing of Tuple

tup = tuple("coastalseven")
print(tup[1:])
print(tup[::-1])
print(tup[4:9])



 